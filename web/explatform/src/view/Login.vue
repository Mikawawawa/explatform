<template>
  <div class="centered-container">
    <md-content class="md-elevation-3">

      <div class="title">
        <!-- <img src="https://vuematerial.io/assets/logo-color.png"> -->
        <div class="md-title">Explatform</div>
        <!-- <div class="md-body-1">Build beautiful apps with Material Design and Vue.js</div> -->
      </div>

      <div class="form">
        <md-field>
          <label>帐号</label>
          <md-input v-model="login.id" autofocus></md-input>
        </md-field>

        <md-field md-has-password>
          <label>密码</label>
          <md-input v-model="login.password" type="password"></md-input>
        </md-field>
      </div>

      <div class="actions md-layout md-alignment-center-space-between" style="">
        <md-button class="md-raised md-primary" @click="auth()">Log in</md-button>
      </div>

      <div class="loading-overlay" v-if="loading">
        <md-progress-spinner md-mode="indeterminate" :md-stroke="2"></md-progress-spinner>
      </div>

      <md-snackbar :md-position="'center'" :md-active.sync="showToast" md-persistent>
        <span>抱歉，服务器吃饭去了，请重试</span>
        <md-button class="md-primary" @click="showToast = false">重试</md-button>
      </md-snackbar>
    </md-content>
    <div class="background" />
  </div>
</template>

<script>
export default {
  name: "Login",
  data() {
    return {
      loading: false,
      showToast:false,
      login: {
        id: "",
        password: "",
        type:""
      }
    };
  },
  methods: {
    async auth(){
      this.loading = true;
      let delay=setTimeout(()=>{
          this.loading = false;
          this.showToast=true
      },8000)
      
      let data=await this.$dataSource.Login(this.login.id,this.login.password)
      if(data.status==1){
        this.$cookie.set("user_id",data.info.user_id)
        this.$cookie.set("user_type",data.info.user_type)
        this.$store.commit("setUser",JSON.stringify(data.info))
      
        this.loading = false;
        this.$router.push("/");
        clearInterval(delay)
      }else{
        setTimeout(()=>{
          this.loading=false
          this.showToast=true
          clearInterval(delay)
        },800)
      }
      }
    }
};
</script>

